from tkinter import *
import time

class Tik:
    def __init__(self):
        self.id = canvas.create_oval(40, 20, 70, 50, fill="white")
        self.y = 3
        self.x = 3

    def draw(self):
        canvas.move(self.id, self.x, self.y)
        bx, by, bx1, by1 = canvas.coords(self.id)
        x1, y1, x11, y11 = canvas.coords(gd.id)
        x2, y2, x22, y22 = canvas.coords(gdps.id)
        if by <= 0 or by1 >= 600:
            self.y = -self.y
        if bx <= 0 or bx1 >= 800:
            return True
        if by > y1 - 25 and by1 < y11 + 25 and bx <= x11:
            self.x -= 0.25
            self.x = -self.x
        if by > y2 and by1 < y22 and bx1 >= x2:
            self.x += 0.25
            self.x = -self.x


class I:
    def __init__(self):
        self.id = None
        self.y = None


    def draw(self):
        canvas.move(self.id, 0, self.y)
        _, y, _, y1 = canvas.coords(self.id)
        if y <= 0 or y1 >= 600:
            self.y = 0


class First_player(I):
    def __init__(self):
        super().__init__()
        self.id = canvas.create_rectangle(30, 10, 40, 90, fill="white")
        self.y = 0
        self.speed = 3

    def move(self, event):
        print(event.__dict__)
        if event.keysym == "w":
            self.y = -self.speed
        if event.keysym == "s":
            self.y = self.speed

    def stop(self, event):
        if event.keysym in "ws":
            self.y = 0

class I_am_me(I):
    def __init__(self):
        super().__init__()
        self.id = canvas.create_rectangle(760, 10, 770, 90, fill="white")
        self.y = 0
        self.speed = 3
    def move(self, event):
        print(event.__dict__)
        if event.keysym == "Up":
            self.y = -self.speed
        if event.keysym == "Down":
            self.y = self.speed

    def stop(self, event):
        if event.keysym in ("Up","Down"):
            self.y = 0


root = Tk()
root.geometry("800x600")
root.title("tic tac toe")

canvas = Canvas(root, width=800, height=600, bg="black")
canvas.pack()
gd = First_player()
gdps = I_am_me()
oval = Tik()
label = Label(text = 'you loser', fg = 'white')
root.bind_all("<KeyPress>", gd.move)
root.bind_all("<KeyPress>", gdps.move, add="+")
root.bind_all("<KeyRelease>", gdps.stop, add="+")
root.bind_all("<KeyRelease>", gd.stop)

while True:
    root.update()
    root.update_idletasks()
    loser = oval.draw()
    if loser:
        canvas.create_text(400,200,text='you loser', fill='white')
    gd.draw()
